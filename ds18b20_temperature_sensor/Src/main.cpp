#include "main.h"
#include "ds18b20.h"

void SystemClock_Config(void);
static void MX_GPIO_Init(void);

bool select = false;

ds18b20 TEMP1, TEMP2;

/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();

	TEMP1.Start(DS18B20_SENSOR1);
	TEMP2.Start(DS18B20_SENSOR2);
	
  while (1)
	{
		if(!select)
		{
			TEMP1.Task();
			select = true;
		}
		else
		{
			TEMP2.Task();
			select = false;
		}
  }
}

